// Primitive Datatype: Number, String, Boolean
console.log("================== Number Datatype================");
var age=22;
console.log("Age is:", age);
var ageTypeof  = typeof age;
console.log("Datatype of age is:",ageTypeof);

console.log("================== String Datatype================");
var city='Solapur'
console.log("City is:", city);
var cityTypeof = typeof city;
console.log("Datatype of city is:",cityTypeof);

console.log("================== String Datatype================");
var marks="99";
console.log("Marks is:", marks);
var marksTypeof = typeof marks;
console.log("Datatype of marks is:", marksTypeof );

console.log("================== Boolean Datatype================");
var isMarried = true;
console.log("isMarried:",isMarried);
var isMarriedTypeof = typeof isMarried;
console.log("Datatype od isMarried is:",isMarriedTypeof);

var isFailed = false;
console.log("isFailed :",isFailed );
var isFailedTypeof = typeof isFailed ;
console.log("Datatype od isFailed  is:",isFailedTypeof );


console.log("================== Special Datatype: Undefined ================");
var country;
console.log("Country value is:", country);
var countryTypeof = typeof country;
console.log("Datatype of country is:",countryTypeof );


console.log("================== Special Datatype:Null ================");
var village = null;
console.log("Village value is:",village);
var villageTypeof = typeof village;
console.log("Datatype of village is:",villageTypeof );







